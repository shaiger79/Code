    def _normalize_core_cols(self, df):
        if df.empty: return df
        cmap = {}
        for c in df.columns:
            cl = str(c).strip().lower().replace('_', '').replace('-', '').replace(' ', '')
            if cl in ['enodebid', 'enbid', 'neid', 'nodeid', 'neuniqueid', 'systemid']: cmap[c] = 'eNB_ID'
            elif cl in ['sector', 'sec']: cmap[c] = 'Sector'
            elif cl == 'catid': cmap[c] = 'Cat_ID'
        df.rename(columns=cmap, inplace=True)
        return df

    def _extract_int_id(self, series):
        s = series.astype(str).str.extract(r'(\d+)')[0]
        res = pd.to_numeric(s, errors='coerce')
        res = np.where(res.isna(), -1, res)
        return pd.Series(res).astype(int).astype(str).replace('-1', '').values

