    def _create_tree_in_frame(self, parent):
        scroll_y = ttk.Scrollbar(parent, orient="vertical")
        scroll_x = ttk.Scrollbar(parent, orient="horizontal")
        tree = ttk.Treeview(parent, show='headings', yscrollcommand=scroll_y.set, xscrollcommand=scroll_x.set)
        scroll_y.config(command=tree.yview); scroll_x.config(command=tree.xview)
        scroll_y.pack(side=tk.RIGHT, fill=tk.Y); scroll_x.pack(side=tk.BOTTOM, fill=tk.X)
        tree.pack(side=tk.LEFT, fill=tk.BOTH, expand=True)
        return tree

    def _update_tree(self, tree, df):
        tree.delete(*tree.get_children())
        if df.empty: return
        tree["columns"] = list(df.columns)
        for col in df.columns:
            tree.heading(col, text=col); tree.column(col, width=130, anchor=tk.CENTER)
        for _, row in df.iterrows():
            formatted = [f"{v:,.2f}" if isinstance(v, (float, np.floating)) else v for v in row]
            tree.insert("", tk.END, values=formatted)

    def _update_tree_precise(self, tree, df, precise_cols=()):
        """[Learning Energy Curve 전용] Slope처럼 값이 매우 작은 컬럼은 유효숫자 기준으로,
        나머지 실수 컬럼은 기존과 동일하게 소수점 2자리로 표시한다."""
        tree.delete(*tree.get_children())
        if df.empty: return
        tree["columns"] = list(df.columns)
        for col in df.columns:
            tree.heading(col, text=col); tree.column(col, width=140, anchor=tk.CENTER)
        for _, row in df.iterrows():
            formatted = []
            for col, v in row.items():
                if isinstance(v, (float, np.floating)) and pd.notna(v):
                    formatted.append(f"{v:.6g}" if col in precise_cols else f"{v:,.2f}")
                elif isinstance(v, (float, np.floating)):
                    formatted.append("N/A")
                else:
                    formatted.append(v)
            tree.insert("", tk.END, values=formatted)

    def _calc_all_savings(self, df_energy_stat=None):
        """[수정] 누적 NC2 및 Gamma 기반 절감량 + board-type + 시간단위 에너지량 합산"""
        logging.info("========== [DEBUG] 에너지 절감 예측 연산 시작 (Gamma & Board-Type 포함) ==========")
        if getattr(self, 'latest_optimizer_results', None) is None or self.latest_optimizer_results.empty:
            return pd.DataFrame()
        if self.cm_map_df_full.empty or self.ru_spec_df_internal.empty:
            return pd.DataFrame()

        df_opt = self.latest_optimizer_results.copy()
        gamma = getattr(self, 'pred_gamma', tk.DoubleVar(value=0.700)).get()

        group_cols = ['eNodeBID', 'Sector']
        if 'Operating Hours' in df_opt.columns: group_cols.append('Operating Hours')

        saving_records = []
        for keys, group in df_opt.groupby(group_cols):
            group = group.sort_values('ES Level', ascending=False)
            cum_nc2 = 0

            for _, row in group.iterrows():
                es_level = row['ES Level']
                nc2 = int(row.get('Nc2', 0))
                target_cells_str = str(row.get('Target Cell Num', ''))

                cum_nc2 += nc2
                effective_nc2 = cum_nc2
                eff_nc2_gamma = effective_nc2 * gamma  # Gamma 보정 적용

                cells = re.findall(r'\d+', str(target_cells_str))
                if not cells or eff_nc2_gamma == 0:
                    continue

                enodeb = row['eNodeBID']
                cm_sub = self.cm_map_df_full[(self.cm_map_df_full['eNB_ID'].astype(str) == str(enodeb)) & (self.cm_map_df_full['cell-num'].astype(str).isin(cells))]
                if cm_sub.empty: continue

                ru_keys = ['ru-board-id', 'ru-port-id', 'ru-cascade-id']
                avail_keys = [c for c in ru_keys if c in cm_sub.columns]

                spec_bcol = next((c for c in self.ru_spec_df_internal.columns if str(c).strip().lower().replace('-', '').replace('_', '').replace(' ', '') in ['boardtype', 'ruboardtype']), None)
                btype_col = next((c for c in cm_sub.columns if str(c).strip().lower().replace('-', '').replace('_', '').replace(' ', '') in ['boardtype', 'ruboardtype']), None)

                if not spec_bcol or not btype_col: continue

                total_power_diff = 0.0
                b_types = []
                ru_energy_sum = 0.0
                unique_rus = pd.DataFrame()

                if avail_keys:
                    unique_rus = cm_sub[avail_keys + [btype_col]].drop_duplicates(subset=avail_keys)

                    # 시간 포맷팅 (Energy 데이터 추출용)
                    hours_str = str(row.get('Operating Hours', ''))
                    h_list = [int(h) for h in hours_str.split(',') if h.strip().isdigit()]

                    for _, ru in unique_rus.iterrows():
                        b_type = str(ru[btype_col]).strip()
                        if b_type not in b_types: b_types.append(b_type)

                        spec_row = self.ru_spec_df_internal[self.ru_spec_df_internal[spec_bcol].astype(str).str.strip().str.lower() == b_type.lower()]
                        if not spec_row.empty:
                            try:
                                idle = float(spec_row.iloc[0].get('Idle', 0))
                                pa_off = float(spec_row.iloc[0].get('PA off', 0))
                                coe_val = spec_row.iloc[0].get('Coe_paoff', 1.0)
                                coe_paoff = float(coe_val) if pd.notna(coe_val) and str(coe_val).strip() != '' else 1.0
                                total_power_diff += (idle - pa_off) * coe_paoff
                            except: pass

                        # 해당 RU의 소모 시간단위 에너지량 합산 (Energy Stat 연동)
                        if df_energy_stat is not None and not df_energy_stat.empty:
                            mask = pd.Series(True, index=df_energy_stat.index)
                            if h_list: mask &= df_energy_stat['HourOfDay'].isin(h_list)
                            for k in avail_keys:
                                if k in df_energy_stat.columns: mask &= (df_energy_stat[k] == ru[k])

                            ru_data = df_energy_stat[mask]
                            if not ru_data.empty:
                                ru_hr = ru_data.groupby('Hourly_TS', observed=True).agg({'RuPowerTot':'sum', 'RuPowerCnt':'sum'})
                                ru_hr['Energy_Wh'] = np.where(ru_hr['RuPowerCnt']>0, ru_hr['RuPowerTot']/ru_hr['RuPowerCnt'], 0)
                                ru_energy_sum += ru_hr['Energy_Wh'].sum()

                # 보정된 eff_nc2_gamma로 절감량 산출
                saving_wh = (total_power_diff * eff_nc2_gamma) / 4.0

                record = {
                    'eNodeBID': str(enodeb), 'Sector': str(row['Sector']), 'ES Level': es_level,
                    'Target Cell Num': target_cells_str, 'Board Type': ",".join(b_types),
                    'Target RU Count': len(unique_rus), 'Target RU Consumed [Wh]': round(ru_energy_sum, 2),
                    'Power Delta [W]': total_power_diff, 'Original Nc2': nc2,
                    'Effective Nc2': effective_nc2, 'Gamma (γ)': gamma, 'Corr. Nc2': round(eff_nc2_gamma, 2),
                    'Est. Saving [Wh]': round(saving_wh, 2)
                }
                if 'Operating Hours' in df_opt.columns: record['Operating Hours'] = row['Operating Hours']
                saving_records.append(record)

        return pd.DataFrame(saving_records)

