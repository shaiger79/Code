    def _prep_traffic_data_for_patterns(self, selected_enodebs, cat_id):
        traffic_df = self._read_csv_auto(self.traffic_file.get())
        traffic_df = self._normalize_core_cols(traffic_df)
        traffic_df['eNB_ID'] = self._extract_int_id(traffic_df['eNB_ID'])
        if 'Sector' in traffic_df.columns: traffic_df['Sector'] = self._extract_int_id(traffic_df['Sector'])
        traffic_df = self._filter_date_time(traffic_df, apply_hour_filter=False)
        traffic_df = traffic_df[traffic_df['eNB_ID'].isin(selected_enodebs)].copy()

        conf_df = self._normalize_core_cols(self.sector_df_internal.copy())
        conf_df['eNB_ID'] = self._extract_int_id(conf_df['eNB_ID'])
        if 'Sector' in conf_df.columns: conf_df['Sector'] = self._extract_int_id(conf_df['Sector'])
        if 'Cat_ID' in conf_df.columns: conf_df = conf_df[conf_df['Cat_ID'].astype(str) == cat_id]

        carrier_df = self._normalize_core_cols(self.carrier_df_internal.copy())
        if 'Cat_ID' in carrier_df.columns: carrier_df = carrier_df[carrier_df['Cat_ID'].astype(str) == cat_id]
        if 'DSS' not in carrier_df.columns: carrier_df['DSS'] = 0

        if 'UsedRB_t' not in traffic_df.columns and 'UsedRB' in traffic_df.columns: traffic_df['UsedRB_t'] = traffic_df['UsedRB']
        traffic_df['UsedRB_t'] = np.where(traffic_df['UsedRB_t'] == 0, traffic_df['UsedRB'], traffic_df['UsedRB_t'])

        rrc_col = 'RRCUE' if 'RRCUE' in traffic_df.columns else ('RRC_UE' if 'RRC_UE' in traffic_df.columns else None)
        if not rrc_col:
            traffic_df['RRCUE'] = 0
            rrc_col = 'RRCUE'

        total_rb_map = {}
        grouped_conf = conf_df[conf_df['eNB_ID'].isin(selected_enodebs)].groupby(['eNB_ID', 'Sector'])
        for (enb, sec), group in grouped_conf:
            band_cols = [col for col in group.columns if col.startswith('B')]
            raw_bands = pd.to_numeric(group[band_cols].iloc[0], errors='coerce').fillna(0).astype(float)

            effective_bands = pd.Series(index=raw_bands.index, dtype=float)
            for b_col, b_val in raw_bands.items():
                if b_val == 0:
                    effective_bands[b_col] = 0.0
                    continue
                b_str = b_col.replace('B', '')
                dss_vals = carrier_df.loc[carrier_df['band'].astype(str) == b_str, 'DSS'].values
                effective_bands[b_col] = b_val * 0.5 if len(dss_vals) > 0 and dss_vals[0] == 1 else b_val
            total_rb_map[(str(enb), str(sec))] = effective_bands.abs().sum()

        # [수정] TotalRB_calc가 없으면 100을 디폴트로 주어 PRB Usage가 100%로 뜨는 버그 방지
        def get_total_rb(row):
            val = total_rb_map.get((str(row['eNB_ID']), str(row.get('Sector', ''))), None)
            return val if val else 100.0

        traffic_df['TotalRB_calc'] = traffic_df.apply(get_total_rb, axis=1)
        traffic_df['TotalRB_calc'] = traffic_df['TotalRB_calc'].replace(0, 100.0)

        traffic_df['IP_Tput_Mbps'] = np.where(traffic_df['IpThruThpDLTime'] > 0, (traffic_df['IpThruThpVoDLByte'] / traffic_df['IpThruThpDLTime']) * 8, 0.0)
        traffic_df['PRB_usage_pct'] = (traffic_df['UsedRB_t'] / traffic_df['TotalRB_calc']) * 100
        traffic_df['PRB_usage_pct'] = traffic_df['PRB_usage_pct'].clip(upper=100.0)
        traffic_df['RRC_UE_val'] = traffic_df[rrc_col]

        return traffic_df.sort_values(by=['eNB_ID', 'Sector', 'Time'])

    def _get_metric_configs(self, df):
        max_airmac = df['AirMacDLByte'].max() * 1.1 if df['AirMacDLByte'].max() > 0 else 100
        max_iptput = max(df['IP_Tput_Mbps'].max() * 1.1, self.tput_threshold.get() * 1.5)
        max_rrcue = df['RRC_UE_val'].max() * 1.1 if df['RRC_UE_val'].max() > 0 else 10
        return {
            'AirMacDLBytes [Kbytes]': ('AirMacDLBytes [Kbytes]', 'AirMacDLByte', max_airmac),
            'IP Tput [Mbps]': ('IP Tput [Mbps]', 'IP_Tput_Mbps', max_iptput),
            'IP Tput [Mbps] (Max 30)': ('IP Tput [Mbps] (Max 30)', 'IP_Tput_Mbps', 30),
            'PRB usage [%]': ('PRB usage [%]', 'PRB_usage_pct', 105),
            'RRC UE': ('RRC UE', 'RRC_UE_val', max_rrcue)
        }

