    def _filter_date_time(self, traffic_df, apply_hour_filter=True):
        traffic_df = traffic_df.copy() # [수정] 명시적 복사본 생성으로 SettingWithCopyWarning 원천 차단

        if 'Time' in traffic_df.columns: time_col = 'Time'
        elif 'TIMESTAMP' in traffic_df.columns: time_col = 'TIMESTAMP'
        else: return traffic_df

        traffic_df[time_col] = pd.to_datetime(traffic_df[time_col])

        if HAS_CALENDAR: start_date, end_date = self.start_date_picker.get_date(), self.end_date_picker.get_date()
        else:
            try: start_date, end_date = datetime.strptime(self.start_date_str.get(), '%Y-%m-%d').date(), datetime.strptime(self.end_date_str.get(), '%Y-%m-%d').date()
            except: start_date = end_date = None

        if start_date and end_date: traffic_df = traffic_df[(traffic_df[time_col].dt.date >= start_date) & (traffic_df[time_col].dt.date <= end_date)]
        exclude_str = self.exclude_dates_str.get().strip()
        if exclude_str:
            exc_dates = []
            for d_str in exclude_str.split(','):
                try: exc_dates.append(datetime.strptime(d_str.strip(), '%Y-%m-%d').date())
                except: pass
            if exc_dates: traffic_df = traffic_df[~traffic_df[time_col].dt.date.isin(exc_dates)]

        if apply_hour_filter and not self.auto_optimize_time.get():
            hours = [int(h.strip()) for h in self.time_hours.get().split(",")]
            traffic_df = traffic_df[traffic_df[time_col].dt.hour.isin(hours)]

        if time_col == 'TIMESTAMP':
            traffic_df = traffic_df.rename(columns={'TIMESTAMP': 'Time'}) # [수정] inplace=True 제거 및 재할당

        return traffic_df

