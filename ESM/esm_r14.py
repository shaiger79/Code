    def _run_prediction_calc(self):
        hours_str = self.pred_hours_var.get().strip()

        # 1. Energy Stat 파싱 (시간 필터 적용)
        try:
            df_e = self._parse_energy_stat()
            if hours_str.lower() != 'all':
                h_list = [int(x.strip()) for x in hours_str.split(',')]
                df_e = df_e[df_e['Timestamp'].dt.hour.isin(h_list)]
        except Exception as e:
            df_e = pd.DataFrame()
            logging.warning(f"[DEBUG] Energy Stat 파싱 오류: {e}")

        # 2. 상세 내역 연산 (df_e를 넘겨 RU별 소모 에너지도 계산)
        df_sav_det = self._calc_all_savings(df_energy_stat=df_e)
        if df_sav_det.empty:
            messagebox.showwarning("경고", "Optimizer 결과가 없거나 예측 가능한 절감량이 없습니다.")
            return

        df_sav_sec = df_sav_det.groupby(['eNodeBID', 'Sector'])['Est. Saving [Wh]'].sum().reset_index()

        # 3. Consumed 에너지 연산
        try:
            if df_e.empty: raise ValueError("해당 시간대에 Energy Stat 데이터가 없습니다.")
            ru_keys = ['eNB_ID', 'ru-board-id', 'ru-port-id', 'ru-cascade-id']
            avail_keys = [k for k in ru_keys if k in df_e.columns]

            ru_hourly = df_e.groupby(avail_keys + ['Hourly_TS'], observed=True).agg({'RuPowerTot':'sum', 'RuPowerCnt':'sum'}).reset_index()
            ru_hourly['Energy_Wh'] = np.where(ru_hourly['RuPowerCnt']>0, ru_hourly['RuPowerTot']/ru_hourly['RuPowerCnt'], 0)

            if not self.cm_map_df_full.empty:
                cm_ru = self.cm_map_df_full.copy()
                valid_rus = cm_ru[avail_keys + ['Sector']].drop_duplicates(subset=avail_keys, keep='first')
                ru_hourly = pd.merge(ru_hourly, valid_rus, on=avail_keys, how='inner')
            else:
                ru_hourly['Sector'] = ''

            ru_hourly.rename(columns={'eNB_ID': 'eNodeBID'}, inplace=True)
            ru_hourly['eNodeBID'] = ru_hourly['eNodeBID'].astype(str)
            ru_hourly['Sector'] = ru_hourly['Sector'].astype(str)

            df_cons_sec = ru_hourly.groupby(['eNodeBID', 'Sector'], observed=True)['Energy_Wh'].sum().reset_index()
            df_cons_sec.rename(columns={'Energy_Wh': 'Consumed [Wh]'}, inplace=True)

        except Exception as e:
            df_cons_sec = pd.DataFrame(columns=['eNodeBID', 'Sector', 'Consumed [Wh]'])

        if not df_cons_sec.empty:
            df_sec_summary = pd.merge(df_cons_sec, df_sav_sec, on=['eNodeBID', 'Sector'], how='outer').fillna(0)
        else:
            df_sec_summary = df_sav_sec.copy()
            df_sec_summary['Consumed [Wh]'] = 0.0

        df_sec_summary['Expected [Wh]'] = df_sec_summary['Consumed [Wh]'] - df_sec_summary['Est. Saving [Wh]']
        df_sec_summary['Expected [Wh]'] = df_sec_summary['Expected [Wh]'].apply(lambda x: x if x > 0 else 0)
        df_sec_summary['Saving Ratio [%]'] = np.where(df_sec_summary['Consumed [Wh]'] > 0,
                                                       (df_sec_summary['Est. Saving [Wh]'] / df_sec_summary['Consumed [Wh]']) * 100, 0.0)

        # 4. Traffic Data 로드 및 EE(Energy Efficiency) 연산
        try:
            df_t = self._read_csv_auto(self.traffic_file.get())
            df_t = self._normalize_core_cols(df_t)
            df_t = self._filter_date_time(df_t, apply_hour_filter=False)
            if 'eNB_ID' in df_t.columns: df_t['eNB_ID'] = self._extract_int_id(df_t['eNB_ID'])
            if 'Sector' in df_t.columns: df_t['Sector'] = self._extract_int_id(df_t['Sector'])

            for c in ['AirMacDLByte', 'AirMacULByte']:
                if c not in df_t.columns: df_t[c] = 0.0

            if hours_str.lower() != 'all' and 'Time' in df_t.columns:
                h_list = [int(x.strip()) for x in hours_str.split(',')]
                df_t = df_t[df_t['Time'].dt.hour.isin(h_list)]

            df_t['Total_Traffic_Byte'] = df_t['AirMacDLByte'].fillna(0) + df_t['AirMacULByte'].fillna(0)
            df_t_sec = df_t.groupby(['eNB_ID', 'Sector'])['Total_Traffic_Byte'].sum().reset_index()
            df_t_sec.rename(columns={'eNB_ID': 'eNodeBID'}, inplace=True)
            df_t_sec['eNodeBID'] = df_t_sec['eNodeBID'].astype(str)
            df_t_sec['Sector'] = df_t_sec['Sector'].astype(str)
        except Exception as e:
            logging.warning(f"[DEBUG] Traffic EE 연산용 데이터 로드 실패: {e}")
            df_t_sec = pd.DataFrame(columns=['eNodeBID', 'Sector', 'Total_Traffic_Byte'])

        # Sector 데이터에 EE 병합
        if not df_t_sec.empty:
            df_sec_summary = pd.merge(df_sec_summary, df_t_sec, on=['eNodeBID', 'Sector'], how='left').fillna(0)
        else:
            df_sec_summary['Total_Traffic_Byte'] = 0.0


        df_sec_summary['EE_Consumed'] = np.where(df_sec_summary['Consumed [Wh]'] > 0, df_sec_summary['Total_Traffic_Byte'] / df_sec_summary['Consumed [Wh]'], 0)
        df_sec_summary['EE_Expected'] = np.where(df_sec_summary['Expected [Wh]'] > 0, df_sec_summary['Total_Traffic_Byte'] / df_sec_summary['Expected [Wh]'], 0)
        df_sec_summary['EE_Improvement [%]'] = np.where(df_sec_summary['EE_Consumed'] > 0, ((df_sec_summary['EE_Expected'] - df_sec_summary['EE_Consumed']) / df_sec_summary['EE_Consumed']) * 100, 0)

        # [핵심 수정] 여기에 'Total_Traffic_Byte'를 반드시 포함해야 다음 eNodeBID groupby 연산에서 KeyError가 나지 않습니다.
        df_sec_summary = df_sec_summary[['eNodeBID', 'Sector', 'Consumed [Wh]', 'Est. Saving [Wh]', 'Saving Ratio [%]', 'Expected [Wh]', 'Total_Traffic_Byte', 'EE_Consumed', 'EE_Expected', 'EE_Improvement [%]']]

        # 5. 전체 eNodeBID 요약 생성 (Total 행 추가)
        df_enb_summary = df_sec_summary.groupby('eNodeBID', observed=True).agg({
            'Consumed [Wh]': 'sum', 'Est. Saving [Wh]': 'sum', 'Expected [Wh]': 'sum', 'Total_Traffic_Byte': 'sum'
        }).reset_index()

        df_enb_summary['Saving Ratio [%]'] = np.where(df_enb_summary['Consumed [Wh]'] > 0, (df_enb_summary['Est. Saving [Wh]'] / df_enb_summary['Consumed [Wh]']) * 100, 0.0)
        df_enb_summary['EE_Consumed'] = np.where(df_enb_summary['Consumed [Wh]'] > 0, df_enb_summary['Total_Traffic_Byte'] / df_enb_summary['Consumed [Wh]'], 0)
        df_enb_summary['EE_Expected'] = np.where(df_enb_summary['Expected [Wh]'] > 0, df_enb_summary['Total_Traffic_Byte'] / df_enb_summary['Expected [Wh]'], 0)
        df_enb_summary['EE_Improvement [%]'] = np.where(df_enb_summary['EE_Consumed'] > 0, ((df_enb_summary['EE_Expected'] - df_enb_summary['EE_Consumed']) / df_enb_summary['EE_Consumed']) * 100, 0)

        # Total Summary Row 계산
        t_cons = df_enb_summary['Consumed [Wh]'].sum()
        t_sav = df_enb_summary['Est. Saving [Wh]'].sum()
        t_exp = df_enb_summary['Expected [Wh]'].sum()
        t_trf = df_enb_summary['Total_Traffic_Byte'].sum()

        t_sav_ratio = (t_sav / t_cons * 100) if t_cons > 0 else 0
        t_ee_cons = (t_trf / t_cons) if t_cons > 0 else 0
        t_ee_exp = (t_trf / t_exp) if t_exp > 0 else 0
        t_ee_imp = ((t_ee_exp - t_ee_cons) / t_ee_cons * 100) if t_ee_cons > 0 else 0

        total_row = pd.DataFrame([{
            'eNodeBID': 'TOTAL (All eNBs)', 'Consumed [Wh]': t_cons, 'Est. Saving [Wh]': t_sav,
            'Saving Ratio [%]': t_sav_ratio, 'Expected [Wh]': t_exp, 'Total_Traffic_Byte': t_trf,
            'EE_Consumed': t_ee_cons, 'EE_Expected': t_ee_exp, 'EE_Improvement [%]': t_ee_imp
        }])
        df_enb_summary = pd.concat([df_enb_summary, total_row], ignore_index=True)
        df_enb_summary = df_enb_summary[['eNodeBID', 'Consumed [Wh]', 'Est. Saving [Wh]', 'Saving Ratio [%]', 'Expected [Wh]', 'EE_Consumed', 'EE_Expected', 'EE_Improvement [%]']]

        # 6. 상단 UI 라벨 업데이트
        if t_cons >= 1e9: div, unit = 1e9, "GWh"
        elif t_cons >= 1e6: div, unit = 1e6, "MWh"
        elif t_cons >= 1e3: div, unit = 1e3, "kWh"
        else: div, unit = 1, "Wh"

        self.pred_summary_label.config(text=f"Total Consumed: {t_cons/div:,.2f} {unit} | Total Expected: {t_exp/div:,.2f} {unit} | Total Saving: {t_sav/div:,.2f} {unit} (절감률 {t_sav_ratio:.2f}%)")

        self._update_tree(self.tree_enb, df_enb_summary)
        self._update_tree(self.tree_sec, df_sec_summary)
        self._update_tree(self.tree_det, df_sav_det)

        self.pred_df_enb = df_enb_summary
        self.pred_df_sec = df_sec_summary
        self.pred_df_det = df_sav_det


