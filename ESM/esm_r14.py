    def _calculate_est_saving(self, enodeb, target_cells_str, active_samples):
        if not target_cells_str or pd.isna(target_cells_str) or target_cells_str == '-1' or active_samples <= 0: return 0.0
        if self.cm_map_df_full.empty or self.ru_spec_df_internal.empty: return 0.0

        try:
            # [수정] 여러개 셀도 정상적으로 인식
            cells = re.findall(r'\d+', str(target_cells_str))
            if not cells: return 0.0

            cm_sub = self.cm_map_df_full[(self.cm_map_df_full['eNB_ID'].astype(str) == str(enodeb)) & (self.cm_map_df_full['cell-num'].astype(str).isin(cells))]
            if cm_sub.empty: return 0.0

            ru_keys = ['ru-board-id', 'ru-port-id', 'ru-cascade-id']
            avail_keys = [c for c in ru_keys if c in cm_sub.columns]

            spec_bcol = next((c for c in self.ru_spec_df_internal.columns if str(c).strip().lower().replace('-', '').replace('_', '').replace(' ', '') in ['boardtype', 'ruboardtype']), None)
            btype_col = next((c for c in cm_sub.columns if str(c).strip().lower().replace('-', '').replace('_', '').replace(' ', '') in ['boardtype', 'ruboardtype']), None)

            if not spec_bcol or not btype_col: return 0.0

            total_power_diff = 0.0
            if avail_keys:
                unique_rus = cm_sub[avail_keys + [btype_col]].drop_duplicates(subset=avail_keys)
                for _, ru in unique_rus.iterrows():
                    b_type = str(ru[btype_col]).strip().lower()
                    spec_row = self.ru_spec_df_internal[self.ru_spec_df_internal[spec_bcol].astype(str).str.strip().str.lower() == b_type]
                    if not spec_row.empty:
                        try:
                            idle = float(spec_row.iloc[0].get('Idle', 0))
                            pa_off = float(spec_row.iloc[0].get('PA off', 0))
                            coe_val = spec_row.iloc[0].get('Coe_paoff', 1.0)
                            coe_paoff = float(coe_val) if pd.notna(coe_val) and str(coe_val).strip() != '' else 1.0
                            total_power_diff += (idle - pa_off) * coe_paoff
                        except: pass

            return round((total_power_diff * active_samples) / 4.0, 2)
        except: return 0.0

    def _validate_time_set(self, hours):
        if not hours: return []
        hours = sorted(list(set(hours)))
        blocks, current = [], [hours[0]]
        for h in hours[1:]:
            if h == current[-1] + 1: current.append(h)
            else:
                blocks.append(current)
                current = [h]
        blocks.append(current)

        valid_blocks = [b for b in blocks if len(b) >= 2]
        valid_hours = []
        for b in valid_blocks: valid_hours.extend(b)
        if len(valid_hours) >= 4: return valid_hours
        return []

    def _check_threshold_exists(self, hr_group, drb_n, total_rb, alpha, t_target, guarantee_ratio):
        raw_rb_margin = total_rb - hr_group['UsedRB_t_adj'] - drb_n
        rb_margin = np.maximum(raw_rb_margin, 5)
        pred_iptput = (rb_margin * alpha * hr_group['SP']) / 1000

        search_max = int(total_rb - drb_n)
        for v in range(search_max, -1, -1):
            mask_c1 = hr_group['UsedRB'] <= v
            n_c1 = mask_c1.sum()
            if n_c1 > 0:
                mask_c2 = mask_c1 & (pred_iptput >= t_target) & (raw_rb_margin >= 5)
                if mask_c2.sum() / n_c1 >= guarantee_ratio: return True
        return False

    def _evaluate_hourly_category(self, hr_group, max_n, total_rb, positive_bands_values, rb_low, t_target, guarantee_ratio, max_win):
        cat1, cat2, cat3 = False, False, False
        n_low = (hr_group['UsedRB'] <= rb_low).sum()
        if n_low / len(hr_group) >= self.threshold_rblowutil.get(): cat1 = True

        valid_alpha = hr_group[hr_group['InitEstIPTput'] != 0]
        alpha = 0.0
        if not valid_alpha.empty:
            den = (valid_alpha['InitEstIPTput'] ** 2).sum()
            if den != 0: alpha = (valid_alpha['cmIPTput'] * valid_alpha['InitEstIPTput']).sum() / den

        if alpha > 0:
            if max_n >= 2:
                drb_2 = positive_bands_values[:2].sum()
                if self._check_threshold_exists(hr_group, drb_2, total_rb, alpha, t_target, guarantee_ratio): cat2 = True
            if max_n >= 1:
                drb_1 = positive_bands_values[:1].sum()
                if self._check_threshold_exists(hr_group, drb_1, total_rb, alpha, t_target, guarantee_ratio): cat3 = True

        return cat1, cat2, cat3

    def _build_th_cols(self, rb_threshold, total_rb, t_target):
        """[r15] ESM Output Result의 'PRBusage Threshold' 한 열을 Entering/Leaving Th_PRB(RB 사용률 기준)
        + Entering/Leaving Th_Tput(IP Tput 기준) 4개 열로 정리한다.
        - Entering Th_PRB: 기존 PRBusage Threshold와 동일(=RBThreshold/total_rb*100, ES가 진입하는 시점).
        - Leaving Th_PRB: RBThreshold에 RB Threshold Margin_LTE(히스테리시스 여유분)를 더해 ES를 벗어나는
          시점을 더 높게 잡음(=(RBThreshold + RB Threshold Margin_LTE)/total_rb/0.01).
        - Entering/Leaving Th_Tput: 최소보장 목표 IP Tput_LTE에 IP Tput Margin_LTE를 더하거나(Entering) 더하지
          않은(Leaving) 값 — t_target이 이미 tput_threshold+tput_margin이므로 그대로 사용.
        RBThreshold가 -1(ES Level 7, 정책 없음)이거나 total_rb가 0이면 4개 모두 -1로 '해당 없음'을 표시."""
        if rb_threshold is None or rb_threshold < 0 or total_rb <= 0:
            return {'Entering Th_PRB': -1, 'Leaving Th_PRB': -1, 'Entering Th_Tput': -1, 'Leaving Th_Tput': -1}
        margin = self.rb_threshold_margin_lte.get()
        entering_prb = (rb_threshold / total_rb) * 100
        leaving_prb = ((rb_threshold + margin) / total_rb) / 0.01
        return {
            'Entering Th_PRB': round(entering_prb, 2),
            'Leaving Th_PRB': round(leaving_prb, 2),
            'Entering Th_Tput': round(t_target, 2),
            'Leaving Th_Tput': round(self.tput_threshold.get(), 2),
        }

    def _generate_core_policy(self, group, enodeb, sector, max_n, total_rb, positive_bands_names, positive_bands_values, negative_bands_names, carrier_df, target_col, rb_low, t_target, is_low_util, hours_str, suffix, plot_dir):
        sector_results = []
        if is_low_util:
            alpha = 1.0
            fixed_rb_threshold = rb_low * self.coe_low.get()
            active_samples = (group['UsedRB'] <= fixed_rb_threshold).sum()

            for n in range(max_n, 0, -1):
                target_cells = self._get_target_cells_str([positive_bands_names[n-1]], carrier_df, target_col)
                drb_n = positive_bands_values[:n].sum()
